def build_inline_points(cpms_id: int, record_index: int) -> list[tuple]:
    rows = []
    start = datetime(2026, 5, 18)
    for day in range(25):
        measured_at = (start + timedelta(days=day)).strftime("%Y-%m-%d")
        baseline = 100 + record_index * 4
        target_value = baseline + sin(day / 2.7) * 2.8 + ((day % 3) - 1) * 0.45
        if record_index in (1, 3) and day > 17:
            target_value += (day - 17) * 0.75
        others_value = baseline - 0.7 + sin((day + 2) / 3.4) * 1.8
        rows.append(
            (
                cpms_id,
                measured_at,
                round(target_value, 2),
                round(others_value, 2),
            )
        )
    return rows
